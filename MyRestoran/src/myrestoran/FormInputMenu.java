    private void CariKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_CariKeyReleased
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();

        try {
            Connection c = CRUD_Koneksi.getKoneksi();
            Statement s = c.createStatement();

            String sql = "select * from menu where kode_menu like '%" + Cari.getText() + "%' or nama_menu like'%" + Cari.getText() + "%' or harga like'" + Cari.getText() + "%'";
            ResultSet r = s.executeQuery(sql);

            while (r.next()) {
                Object[] o = new Object[5];
                o[0] = r.getString("kode_menu");
                o[1] = r.getString("nama_menu");
                o[2] = r.getString("harga");
                
                model.addRow(o);
            }
            r.close();
            s.close();
        } catch (SQLException e) {
            System.out.println("Terjadi Error");
        }
    }//GEN-LAST:event_CariKeyReleased

