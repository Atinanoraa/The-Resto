    private void bsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bsimpanActionPerformed
        // TODO add your handling code here:
        if(jTextField_kode.getText().equals("") ||jTextField_nama.getText().equals("") || jTextField_harga.getText().equals("")){
            JOptionPane.showMessageDialog(null, "LENGKAPI DATA !", "The Resto", JOptionPane.INFORMATION_MESSAGE);
        }else{
            String kode = jTextField_kode.getText();
            String nama = jTextField_nama.getText();
            String harga = jTextField_harga.getText();

            try {
                long millis=System.currentTimeMillis();
                java.sql.Date date=new java.sql.Date(millis);
                System.out.println(date);
                String tgl = date.toString();
                Connection c = CRUD_Koneksi.getKoneksi();

                String sql = "INSERT INTO menu (kode_menu, nama_menu, harga, tgl_realease) VALUES (?, ?, ?, ?)";

                PreparedStatement p = c.prepareStatement(sql);
                p.setString(1, kode);
                p.setString(2, nama);
                p.setString(3, harga);
                p.setString(4, tgl);

                p.executeUpdate();
                p.close();
                
                JOptionPane.showMessageDialog(null, "Data berhasil tersimpan", "The Resto", JOptionPane.INFORMATION_MESSAGE);

            } catch (SQLException e) {
                System.out.println(e);
                System.out.println("Terjadi Error");
                JOptionPane.showMessageDialog(null, "Data tidak berhasil disimpan", "The Resto", JOptionPane.INFORMATION_MESSAGE);
            } finally {
                loadData();
                kode();
                
                jTextField_nama.setText("");
                jTextField_harga.setText("");
            }
        }
    }//GEN-LAST:event_bsimpanActionPerformed

