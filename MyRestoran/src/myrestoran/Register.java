    private void jButton_registerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_registerActionPerformed
        String fname = jTextField_fullname.getText();
        String username = jTextField_username.getText();
        String pass = String.valueOf(jPasswordField_pass.getPassword());
        String Cpass = String.valueOf(jPasswordField_confirmPass.getPassword());
        String phone = jTextField_phone.getText();
        String gender = "Male";
        
        if(jRadioButton_female.isSelected()){
            gender = "Female";
        }
        
        if(verifyFields()){
            if(!checkUsername(username)){
                PreparedStatement ps;
                ResultSet rs;
                String registerQuery = "INSERT INTO `users`(`full_name`, `username`, `password`, `phone`, `gender`, `picture`) VALUES (?,?,?,?,?,?)";
                try {
                    ps = koneksi.getConnection().prepareStatement(registerQuery);
                    ps.setString(1, fname);
                    ps.setString(2, username);
                    ps.setString(3, pass);
                    ps.setString(4, phone);
                    ps.setString(5, gender);
                    
                    try {
                        if(image_path != null){
                            InputStream image = new FileInputStream(new File(image_path));
                            ps.setBlob(6, image);
                        } 
                        else{
                           ps.setNull(6, java.sql.Types.NULL); 
                        }
                      
                        if(ps.executeUpdate() != 0){
                            JOptionPane.showMessageDialog(null, "Akunmu Telah Terbuat dan Silahkan Login Ulang!");
                            Login lg = new Login();
                            lg.setVisible(true);
                            this.setVisible(false);
                        }else{
                            JOptionPane.showMessageDialog(null, "Error : Check Your Information");
                        }
                    } catch (FileNotFoundException ex) {
                        Logger.getLogger(Register.class.getName()).log(Level.SEVERE, null, ex);
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(Register.class.getName()).log(Level.SEVERE, null, ex);
                }
                
            }
        }
        
    }//GEN-LAST:event_jButton_registerActionPerformed

