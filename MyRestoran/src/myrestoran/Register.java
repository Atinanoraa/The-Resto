    public boolean verifyFields()
    {
        String fname = jTextField_fullname.getText();
        String uname = jTextField_username.getText();
        String phone = jTextField_phone.getText();
        String pass = String.valueOf(jPasswordField_pass.getPassword());
        String Cpass = String.valueOf(jPasswordField_confirmPass.getPassword());
        
        if(fname.trim().equals("") || uname.trim().equals("") || phone.trim().equals("")
                || pass.trim().equals("") || Cpass.trim().equals(""))
        {
            JOptionPane.showMessageDialog(null, "Pastikan Kembali Semua Data Terisi");
            return false;
        }
        else if(!pass.equals(Cpass)){
            JOptionPane.showMessageDialog(null, "Password Tidak Cocok", "Konfirmasi Password", 2);
            return false;
        }
        else{
            return true;
        }
    }
    
    public boolean checkUsername(String username){
        PreparedStatement st;
        ResultSet rs;
        boolean username_exist = false;
        
        String query = "SELECT * FROM `users` WHERE `username` = ?";
        
        try {
            st = koneksi.getConnection().prepareStatement(query);
            st.setString(1, username);
            rs = st.executeQuery();
            
            if(rs.next()){
                username_exist = true;
                JOptionPane.showMessageDialog(null, "Username ini telah digunakan oleh pengguna lain, pilih username lain", "Username failed", 2);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Register.class.getName()).log(Level.SEVERE, null, ex);
        }
        return username_exist;
    }
    
