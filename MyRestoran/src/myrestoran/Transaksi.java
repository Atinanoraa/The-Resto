    private void noNota() {
        try {
            Connection c = CRUD_Koneksi.getKoneksi();
            Statement s = c.createStatement();

            String sql = "SELECT * FROM transaksi ORDER by no_nota desc";
            ResultSet r = s.executeQuery(sql);

            if (r.next()) {
                String nota = r.getString("no_nota").substring(1);
                String AN = "" + (Integer.parseInt(nota) + 1);
                String Nol = "";

                if (AN.length() == 1) {
                    Nol = "00";
                } else if (AN.length() == 2) {
                    Nol = "0";
                } else if (AN.length() == 3) {
                    Nol = "";
                } 

                jTextField_transaksi.setText("N" + Nol + AN);
            } else {
                jTextField_transaksi.setText("N001");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

