    private void pilihMenuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pilihMenuActionPerformed
        if (pilihMenu.getSelectedItem().equals("Pilih Menu")){
            jTextField_kode.setText("");
            jTextField_harga.setText("");
        }else{
        try {
            Connection c = CRUD_Koneksi.getKoneksi();
            Statement s = c.createStatement();

            String sql = "SELECT kode_menu FROM menu WHERE nama_menu ='" + pilihMenu.getSelectedItem() + "'";
            ResultSet r = s.executeQuery(sql);

            while (r.next()) {
                jTextField_kode.setText(r.getString("kode_menu"));
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        try {
            Connection c = CRUD_Koneksi.getKoneksi();
            Statement s = c.createStatement();

            String sql = "SELECT harga FROM menu WHERE nama_menu ='" + pilihMenu.getSelectedItem() + "'";
            ResultSet r = s.executeQuery(sql);

            while (r.next()) {
                jTextField_harga.setText(r.getString("harga"));
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        }
    }//GEN-LAST:event_pilihMenuActionPerformed

