    private void jButton_tambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_tambahActionPerformed
       if(jTextField_transaksi.getText().equals("") ||jTextField_kode.getText().equals("") || pilihMenu.getSelectedItem().equals("")|| jTextField_harga.getText().equals("")|| jTextField_qty.getText().equals("")|| jTextField_total.getText().equals("")){
            JOptionPane.showMessageDialog(null, "LENGKAPI DATA !", "The Resto", JOptionPane.INFORMATION_MESSAGE);
        
        }else{
            String kode = jTextField_kode.getText();
            String pilihmenu = (String)pilihMenu.getSelectedItem();
            String harga = jTextField_harga.getText();
            String qty = jTextField_qty.getText();
            String total = jTextField_total.getText();
           
            try {
                Connection c = CRUD_Koneksi.getKoneksi();
                
                String sql = "INSERT INTO tmp_transaksi VALUES (?, ?, ?, ?, ?, ?)";
                
               
                PreparedStatement p = c.prepareStatement(sql);
                p.setString(1, null);
                p.setString(2, kode);
                p.setString(3, pilihmenu);
                p.setString(4, harga);
                p.setString(5, qty);
                p.setString(6, total);
                
                p.executeUpdate();
                p.close();
            } catch (SQLException e) {
                System.out.println("Terjadi Error");
            } finally {
                noNota();
                jTextField_kode.setText("");
                pilihMenu.setSelectedItem("");
                jTextField_harga.setText("");
                jTextField_qty.setText("");
                jTextField_total.setText("");
                JOptionPane.showMessageDialog(null, "Data berhasil disimpan", "The Resto", JOptionPane.INFORMATION_MESSAGE);
                loadData();
                
                
            }
        }
    }//GEN-LAST:event_jButton_tambahActionPerformed

    private void selesaiTransaksiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_selesaiTransaksiActionPerformed
        if(jTextField3.getText().equals("") ||jTextField1.getText().equals("")){
            JOptionPane.showMessageDialog(null, "LENGKAPI DATA !", "The Resto", JOptionPane.INFORMATION_MESSAGE);
        
        }else{
            String a = jTextField1.getText();
            int ab = Integer.parseInt(String.valueOf(jTextField1.getText()));
              if(ab < 0){
                JOptionPane.showMessageDialog(null, "Uang anda kurang", "The Resto", JOptionPane.INFORMATION_MESSAGE);
                jTextField3.setText("");
                jTextField1.setText("");
              }else{
           try {
            Connection c = CRUD_Koneksi.getKoneksi();
            Statement s = c.createStatement();

            String sql = "SELECT * FROM tmp_transaksi";
            ResultSet r = s.executeQuery(sql);

            while (r.next()) {
                long millis=System.currentTimeMillis();  
                java.sql.Date date=new java.sql.Date(millis);  
                System.out.println(date); 
                String tgl = date.toString();
                String sqla = "INSERT INTO transaksi VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?)";
         
                PreparedStatement p = c.prepareStatement(sqla);
                p.setString(1, jTextField_transaksi.getText());
                p.setString(2, r.getString("kode_menu"));
                p.setString(3, r.getString("nama_menu"));
                p.setString(4, r.getString("harga"));
                p.setString(5, r.getString("qty"));
                p.setString(6, r.getString("total"));
                p.setString(7, jTextField3.getText());
                p.setString(8, jTextField1.getText());
                p.setString(9, tgl);
                
                p.executeUpdate();
                p.close();
                
                
            }
            r.close();
            s.close();
            try{
            Desktop.getDesktop().browse(new URL("http://localhost/Resto/Struk/struk.php?kd="+jTextField_transaksi.getText()).toURI());
            } catch (Exception e){
                System.out.println(e);
            }
        } catch (SQLException e) {
            System.out.println("Terjadi Error");
        }finally{
         try {
            String sqla ="TRUNCATE `tmp_transaksi`";
            java.sql.Connection conn=(Connection)CRUD_Koneksi.getKoneksi();
            java.sql.PreparedStatement pst=conn.prepareStatement(sqla);
            pst.execute();
            JOptionPane.showMessageDialog(null, "TRANSAKSI SELESAI", "The Resto", JOptionPane.INFORMATION_MESSAGE);
            loadData();
            jTextField3.setText("");
            jTextField1.setText("");
            jLabel4.setText("");
            noNota();
            selesaiTransaksi.setEnabled(false);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
           }
              }
         }
    
    }//GEN-LAST:event_selesaiTransaksiActionPerformed

